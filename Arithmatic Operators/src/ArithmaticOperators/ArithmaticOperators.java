package ArithmaticOperators;

public class ArithmaticOperators {
    public static void main(String []args){
        int a=10;
        int b=5;
        int c=13;
        System.out.println("a+b is : "+(a+b));
        System.out.println("a-b is : "+(a-b));
        System.out.println("a*b is : "+(a*b));
        System.out.println("a/b is : "+(a/b));
        System.out.println("a%b is : "+(c%b));
        System.out.println("a++ is : "+(a++));
        System.out.println("a-- is : "+(a--));

    }
}
